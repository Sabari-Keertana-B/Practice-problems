package ArrayProblems;

public class Qno31 {
    public static void main(String[] args) {
        int arr[] = {10,24,16,7,17};
        int n = 15;
        
        for(int i=0;i<arr.length;i++){
            if(arr[i]>n){
                System.out.print(arr[i]+" ");
            }
        }
    }
    
}
